# Crear las listas
list1 = [1,2,3,4,5]
list2 = ['Hello','Word','Python']
list3 = ['Operator','Membership', 100,200]

# Comprobar si 5 está en list1
if 5 in list1:
    print("5 está en list1")
else:
    print("5 no está en list1")

# Comprobar si "Hello" y "Python" están en list2
if 'Hello' in list2 and 'Python' in list2:
    print("'Hello' y 'Python' están en list2")
else:
    print("'Hello' y 'Python' no están en list2")

# Comprobar si list2 es igual a list3
if list2 == list3:
    print("list2 es igual a list3")
else:
    print("list2 no es igual a list3")