# Definición de las variables a, b y c
a = 50
b = 5.0
c = 100

# Definición de la variable d como la suma de a y b
d = a + b

# Definición de la variable e como la resta de a y b
e = a - b

# Definición de la variable f como el resultado de la multiplicación de d y e
f = d * e

# Definición de la variable g como el resultado de la división de f y a
g = f / a

# Definición de la variable h como el módulo de f y a
h = f % a